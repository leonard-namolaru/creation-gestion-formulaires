/**
 * Les alertes pour supprimer, modifier ...
 * 
 * @author Huihui Huang
 */

function deleteConfirm() {
	var confirm= window.confirm("Etes-vous sûre de supprimer ?");
	return confirm;
}

function modifConfirm() {
	var confirm= window.confirm("Etes-vous sûre de modifier ?");
	return confirm;
}

function repondreConfirm() {
	var confirm= window.confirm("Etes-vous sûre de répondre à cette catégorie ?");
	return confirm;
}

